let x = 8
let y = 4

if (x > y) {
	console.log(`x jest większe od y`)
} else {
	console.log(`x jest mniejsze od y`)
}

if (x === y) {
	console.log(`x jest równe y`)
} else {
	console.log(`x nie jest równe y`)
}

if (x !== y) {
	console.log(`x nie jest równe y`)
} else {
	console.log(`x jest równe y`)
}
